/*Método `toLowerCase`:
Enunciado: Crea una función que tome una cadena como entrada, y utilice el método `toLowerCase` 
para convertir todos los caracteres en minúsculas.
Entrada: Una cadena de texto.
Salida: La cadena convertida a minúsculas.

Ejemplo:
Entrada: toLowerCase("HeLLo WoRLd")
Salida: "hello world"*/

const toLowerCase = (str)=>{
    return str.toLowerCase()
}

console.log(toLowerCase("HeLLo WoRLd"));
